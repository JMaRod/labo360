
package com.axity.dinosaurPark.model.tourist;

/**
 * Enum que define los posibles estados de un visitante
 * OUT_PARK, afuera del parque
 * IN_PARK, adentro del parque
 * WARNING, presenta alguna alerta, como enfermedad, accidente o daño
 * DANGER, bajo ataque de dinosaurio
 **/
public enum TouristStatus
{
    OUT_PARK, IN_PARK, WARNING, DANGER
}
